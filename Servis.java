class Servis {
    private String deskripsi;
    private String tanggal;

    public Servis(String deskripsi, String tanggal) {
        this.deskripsi = deskripsi;
        this.tanggal = tanggal;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public String getTanggal() {
        return tanggal;
    }
}